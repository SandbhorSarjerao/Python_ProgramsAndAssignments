"""
    And - - when both are True then only result is True
    Or  - - when at least 1 True then result is True
    Not - - To Reverse/Negate the value
"""

a = 5
b = 4

# And - when both are True then only result is True
print(a < 8 and b > 5)
print(a < 8 and b > 2)

# Or - when at least 1 True then result is True
print(a < 8 or b > 5)
print(a < 8 or b > 2)

# Not
x = True
print(not x)


